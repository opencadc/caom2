package ca.nrc.cadc.caom2;

import ca.nrc.cadc.caom2.wcs.ObservableAxis;
import ca.nrc.cadc.caom2.wcs.PolarizationWCS;
import ca.nrc.cadc.caom2.wcs.SpatialWCS;
import ca.nrc.cadc.caom2.wcs.SpectralWCS;
import ca.nrc.cadc.caom2.wcs.TemporalWCS;
import java.util.Date;

/**
 * A Chunk (of a Part) is one quantitatively specified subsection of the whole.
 * The exact subsection is defined as a range (or ranges) in the dimensions of the
 * single data array (the Part). For example, one could divide a 2-dimensional
 * array into arbitrary Chunks of NxM (tiles) in order to describe the range
 * of coordinates in each Chunk without specifying the coordinates of every pixel
 * (usually because the relation is non-linear).
 * 
 * @author pdowler
 */
public class Chunk extends AbstractCaomEntity implements Comparable<Chunk>
{
    private static final long serialVersionUID = 201110261400L;

    // mutable state
    public ProductType productType;

    public Integer naxis;
    public Integer observableAxis;
    public Integer positionAxis1;
    public Integer positionAxis2;
    public Integer energyAxis;
    public Integer timeAxis;
    public Integer polarizationAxis;

    public ObservableAxis observable;
    public SpatialWCS position;
    public SpectralWCS energy;
    public TemporalWCS time;
    public PolarizationWCS polarization;

    public Chunk() { }

    @Override
    public String toString()
    {
        return getClass().getSimpleName() + "[...]";
    }

    public int compareTo(Chunk o)
    {
        int lhs = hashCode();
        int rhs = o.hashCode();
        if (lhs < rhs)
            return -1;
        if (lhs > rhs)
            return 1;
        return 0; // same hashCode == same object
    }

    // the java.lang.Object implementations of equals() and hashCode() are sufficient


}
